    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        LABBoletaBLL co = new LABBoletaBLL();
        LABClienteDAL cli = new LABClienteDAL(); 
        LABBoletaDAL  c = new LABBoletaDAL ();
        
        try 
        {
        int cantidad =Integer.parseInt(txtCantidad.getText());
        int monto=Integer.parseInt(txtPrecio.getText());
        int total=0;
        int temp= Integer.parseInt(txtTotal.getText());
        total = temp + (monto*cantidad);    
        this.txtTotal.setText(String.valueOf(total));
        
        co.setId_boleta(Integer.parseInt(idBoleta2.getText()));
        co.setCantidad(Integer.parseInt(txtCantidad.getText()));
        co.setId_producto(Integer.parseInt(txtIDProducto.getText()));
        co.setTotal(Integer.parseInt(txtTotal.getText()));
        cli.agregarDetalle(co);
        JOptionPane.showMessageDialog(null, "Tour Agregado"); 
        int res=0;
        res=c.actualizarTotal(co);
        
            if (res == -1) {
                JOptionPane.showMessageDialog(null, "erro al actualizar total"); 
            }
        }       
        
        catch (Exception e) 
        { JOptionPane.showMessageDialog(null, "erro al ingresar producto...");  
        }
       
      
      try 
         {
             
       LABBoletaDAL per = new LABBoletaDAL();
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("id Detalle Boleta");
        modelo.addColumn("Pruducto");
        modelo.addColumn("Precio");
        modelo.addColumn("Cantidad");
        
        for(LABBoletaBLL p : per.mostrarDetalleBoleta(Integer.parseInt(this.idBoleta2.getText())))
        {
            Object[] fila = new Object[12];
            
            fila[0] = p.getId_boleta();
            fila[1] = p.getProducto();
            fila[2] = p.getPrecio();  
            fila[3] = p.getCantidad();  
             
            modelo.addRow(fila);
        }
        LABBoleta.jTable1.setModel(modelo);
         } 
         
         catch (Exception e) 
         {
            JOptionPane.showMessageDialog(null, "erro al generar ...");
         } 
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

