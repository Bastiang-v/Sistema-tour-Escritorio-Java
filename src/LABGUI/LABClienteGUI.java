    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        LABClienteDAL client = new LABClienteDAL();
        try {
             ((DefaultTableModel)tablamcliente.getModel()).setRowCount(0);
            for(LABCliente cl : client.mostrarCliente())
        {
            Object[] fila = new Object[5];
            fila[0] = client.FormatoRutUsuario(cl.getRun()+cl.getDv());
            fila[1] = cl.getNombre();
            fila[2] = cl.getApellido();
            fila[3] = cl.getCorreo();
            fila[4] = cl.getSaldo();
          ((DefaultTableModel)tablamcliente.getModel()).addRow(fila);
        }
          JOptionPane.showMessageDialog(null, "Lista Actualizada Correctamente \n Total : "+ ((DefaultTableModel)tablamcliente.getModel()).getRowCount()+" Registros", "Lista Actualizada", JOptionPane.INFORMATION_MESSAGE); 
            ((DefaultTableModel)tablamcliente.getModel()).getRowCount();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

