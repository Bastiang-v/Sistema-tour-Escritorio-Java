    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
       LABCliente cl = new LABCliente();
       LABClienteDAL client = new LABClienteDAL();
      try {
          if (client.validarRut(this.txtrutagregarusuario.getText()+this.txtdvausuario.getText())) {
             cl = client.buscarCliente(this.txtrutagregarusuario.getText());
              if (cl.getEstado()!=1) {
                  cl.setNombre(this.txtnombreausuario.getText());
                  cl.setApellido(this.txtapellidoausuario.getText());
                  cl.setRun(this.txtrutagregarusuario.getText());
                  cl.setDv(this.txtdvausuario.getText());
                  cl.setCorreo(this.txtcorreoausuario.getText());
                  cl.setSaldo(Integer.parseInt(this.txtsaldoausuario.getText()));
                  if (client.agregarCLiente(cl)==1) {
               JOptionPane.showMessageDialog(null, "Cliente Agregado exitosamente \n "+cl.toString(), "CLiente Agregado", JOptionPane.INFORMATION_MESSAGE);
               txtnombreausuario.setText("Nombres");
               txtapellidoausuario.setText("Apellidos");
               txtrutagregarusuario.setText("RUT");
               txtdvausuario.setText("DV");
               txtcorreoausuario.setText("Correo");
               txtsaldoausuario.setText("Saldo");
                  }else{
                    JOptionPane.showMessageDialog(null, "Cliente No Agregado", "Error: CLiente no Agregado", JOptionPane.WARNING_MESSAGE);   
                  }
              }else{
                   JOptionPane.showMessageDialog(null, "El Rut Ingresado ya se encuentra Registrado ", " ERROR: RUT Ya Ingresado", JOptionPane.WARNING_MESSAGE);
              }
          }else{
               JOptionPane.showMessageDialog(null, "Ingrese un rut valido", "RUT Invalido", JOptionPane.WARNING_MESSAGE);
          }
            
        } catch (Exception e) {
              JOptionPane.showMessageDialog(null, "Verifique el Texto Ingresado", "Error al Registrar", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnguardarActionPerformed

